const mongoose = require("mongoose");

const hotelSchema = new mongoose.Schema({
  hotelname: {
    type: String,
    require: true,
  },
  username: {
    type: String,
    require: true,
    unique: true,
  },
  email: {
    type: String,
    require: true,
  },
  password: {
    type: String,
    require: true,
  },
});

module.exports = mongoose.model("Hotel", hotelSchema);
